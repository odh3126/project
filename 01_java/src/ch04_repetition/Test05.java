package ch04_repetition;

public class Test05 {
	public static void main(String[] args) {
		/*
		*/
		for (int i = 1; i < 11; i++) {
			System.out.println(i);
		}
		for (int i = 1; i < 11; i++) {
			System.out.println(i);
		}
		int i = 1;
		while (i < 11) {
			System.out.println(i);
			i++;
		}
		i = 1;
		while (i < 11) {
			System.out.println(i);
			i++;
		}
	}
}













