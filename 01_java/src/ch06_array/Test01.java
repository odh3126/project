package ch06_array;

public class Test01 {
	public static void main(String[] args) {
		// 배열을 만든다.(arr 이름의 배열, 정수를 담는 배열)
		// new int[3] : int값을 3개 담을 수 있는 공간을 새롭게 만들어라..
		// 같은 타입의 값을 관리한다.
		int[] arr = new int[3];
		
		// 배열의 크기 : 배열의이름.length
		System.out.println("arr : " + arr);  // 참조하는 공간을 접근할 수 있는 주소
		System.out.println("arr 크기 : " + arr.length);  // 생성된 배열의 크기
		
		
		
		
		
		
	}
}














