package ch02_operator.exam;

public class Quiz09 {
	public static void main(String[] args) {
		int a = 5;
		System.out.println(++a);  // 6
		// a = 6
		System.out.println(a--);  // 6
		// a = 5
		System.out.println(a);    // 5
		System.out.println(a++);  // 5
		// a = 6
		System.out.println(--a);  // 5
	}
}
